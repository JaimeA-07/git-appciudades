package com.example.fixuser.entidades;

public class Ciudad {

    private Integer cod;
    private String id;
    private String ciu;

    // Constructor

    public Ciudad() {
        this.cod = cod;
        this.id = id;
        this.ciu = ciu;
    }

    // Getter and Setter

    public Integer getCod() {
        return cod;
    }

    public void setCod(Integer cod) {
        this.cod = cod;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCiu() {
        return ciu;
    }

    public void setCiu(String ciu) {
        this.ciu = ciu;
    }

}
